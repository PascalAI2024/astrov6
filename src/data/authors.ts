// Helper function to get random author
export function getRandomAuthor() {
  const authorKeys = Object.keys(authors);
  const randomKey = authorKeys[Math.floor(Math.random() * authorKeys.length)];
  return authors[randomKey];
}

// Helper function to get multiple random authors
export function getRandomAuthors(count: number) {
  const authorKeys = Object.keys(authors);
  const shuffled = [...authorKeys].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count).map(key => authors[key]);
}